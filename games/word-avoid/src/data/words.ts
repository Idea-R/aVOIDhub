export const getRandomGeometricPattern = (difficulty?: Difficulty): GeometricPattern => {
  const patterns = difficulty 
    ? geometricPatterns.filter(p => p.difficulty === difficulty)
    : geometricPatterns;
  
  if (patterns.length === 0) {
    // Fallback to any pattern if none match the difficulty
    return geometricPatterns[Math.floor(Math.random() * geometricPatterns.length)];
  }
  
  return patterns[Math.floor(Math.random() * patterns.length)];
};

